    private void mostrarClientes() {
    conexion dbc = new conexion(); // creamos la conexion
    Connection con = dbc.conectar(); // abrimos la conexion
    
    Statement st = null;
    ResultSet rs = null;
    
    try {
        st = con.createStatement(); // preparamos la consulta
        rs = st.executeQuery("SELECT * FROM clientes"); // ejecutamos la consulta para obtener todos los clientes
        
        // vamos leyendo cada registro que nos devuelve la consulta
        while (rs.next()) {
            // tomamos todos los datos de la fila y los agregamos en el arreglo fila
            Object[] fila = {
                rs.getInt("cliente_id"),      // el ID del cliente (auto_increment)
                rs.getString("nombre"),       // el nombre del cliente (varchar 50)
                rs.getString("apellido"),     // el apellido del cliente (varchar 50)
                rs.getString("email"),        // el email del cliente (varchar 100, unico)
                rs.getString("telefono")      // el telefono del cliente (varchar 15, puede ser null)
            };
            // a√±adimos esa fila a la tabla 
            modelo.addRow(fila);
        }
        
    } catch (SQLException ex) {
        // si algo sale mal con la conexion muestra este mensaje en la consola
        System.out.println("Error al cargar clientes: " + ex.getMessage());
    } finally {
        // cerramos todo para liberar memoria y evitar problemas
        try {
            if (rs != null) rs.close();
            if (st != null) st.close();
            if (con != null) con.close();
        } catch (SQLException ex) {
            System.out.println("Error cerrando recursos: " + ex.getMessage());
        }
    }
}

